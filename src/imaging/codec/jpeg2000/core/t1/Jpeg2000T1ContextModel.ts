export function getSignCodingContext(flags: number): number {
  let index = 0;

  if ((flags & T1_SIG_W) !== 0) {
    index |= (1 << 3);
    if ((flags & T1_SIGN_W) !== 0) {
      index |= (1 << 0);
    }
  }

  if ((flags & T1_SIG_N) !== 0) {
    index |= (1 << 1);
    if ((flags & T1_SIGN_N) !== 0) {
      index |= (1 << 4);
    }
  }

  if ((flags & T1_SIG_E) !== 0) {
    index |= (1 << 5);
    if ((flags & T1_SIGN_E) !== 0) {
      index |= (1 << 2);
    }
  }

  if ((flags & T1_SIG_S) !== 0) {
    index |= (1 << 7);
    if ((flags & T1_SIGN_S) !== 0) {
      index |= (1 << 6);
    }
  }

  return LUT_CTXNO_SC[index] ?? CTXSCSTART;
}

export function getZeroCodingContext(flags: number, orientation: number): number {
  let index = 0;

  if ((flags & T1_SIG_NW) !== 0) {
    index |= (1 << 0);
  }
  if ((flags & T1_SIG_N) !== 0) {
    index |= (1 << 1);
  }
  if ((flags & T1_SIG_NE) !== 0) {
    index |= (1 << 2);
  }
  if ((flags & T1_SIG_W) !== 0) {
    index |= (1 << 3);
  }
  if ((flags & T1_SIG_E) !== 0) {
    index |= (1 << 5);
  }
  if ((flags & T1_SIG_SW) !== 0) {
    index |= (1 << 6);
  }
  if ((flags & T1_SIG_S) !== 0) {
    index |= (1 << 7);
  }
  if ((flags & T1_SIG_SE) !== 0) {
    index |= (1 << 8);
  }

  const safeOrientation = orientation < 0 || orientation > 3 ? 0 : orientation;
  const offset = safeOrientation * 512;
  return LUT_CTXNO_ZC[offset + index] ?? CTXZCSTART;
}

export function getMagRefinementContext(flags: number): number {
  let context = CTXMRSTART;
  if ((flags & T1_SIG_NEIGHBORS) !== 0) {
    context = CTXMRSTART + 1;
  }
  if ((flags & T1_REFINE) !== 0) {
    context = CTXMRSTART + 2;
  }
  return context;
}

